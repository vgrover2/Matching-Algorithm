    public ArrayList<LinkUser> LinkList(){
        ArrayList<LinkUser> usersList = new ArrayList<>(); 
        try{
            Class.forName("org.sqlite.JDBC"); 
            Connection con = DriverManager.getConnection("jdbc:sqlite:/Users/vedantgrover/Desktop/internalassessment.db"); 
            String query1="SELECT * FROM Link";
            Statement s = con.createStatement();
            TableColumnModel columnModel = LinkTable.getColumnModel();
            columnModel.getColumn(0).setPreferredWidth(1200);
            columnModel.getColumn(1).setPreferredWidth(1300);
            columnModel.getColumn(2).setPreferredWidth(1200);
            columnModel.getColumn(3).setPreferredWidth(1200);
            columnModel.getColumn(4).setPreferredWidth(1200);
            LinkTable.setRowHeight(45);
            ResultSet rs = s.executeQuery(query1);
            LinkUser user; 
            while (rs.next()){
                user = new LinkUser(rs.getInt("LinkNo"),rs.getInt("Person_ID"), rs.getInt("TopicNo"), rs.getInt("LevelOnTopic"), rs.getInt("PassionLevel")); 
                usersList.add(user);   
            }   
            
            //s.close();
        }
        catch(ClassNotFoundException | SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
        return usersList; 
    }
    public void show_user(){
        ArrayList<LinkUser> list = LinkList();
        DefaultTableModel model = (DefaultTableModel)LinkTable.getModel();
        Object[] row = new Object[5];
        for(int i=0; i<list.size(); i++ ){
            row[0] = list.get(i).getLinkNo();
            row[1] = list.get(i).getPerson_ID();
            row[2] = list.get(i).getTopicNo();
            row[3] = list.get(i).getLevelOnTopic();
            row[4] = list.get(i).getPassionLevel();
            model.addRow(row);
        }
    
    }
    

