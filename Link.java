    private void SearchLinkButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchLinkButtonActionPerformed
       try{
            String sql = "select * from Link where LinkNo =?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, SearchTextField.getText());
            ResultSet rs = pst.executeQuery();
            if(rs.next()){
                String add1 = rs.getString("LinkNo");
                LinkNoTextField.setText(add1);
                String add2 = rs.getString("Person_ID");
                Person_IDTextField.setText(add2);
                String add3 = rs.getString("TopicNo");
                TopicNoTextField.setText(add3);
                String add4 = rs.getString("LevelOnTopic");
                LevelOnTopicjSpinner.setValue(add4);
                String add5 = rs.getString("PassionLevel");
                PassionLeveljSpinner.setValue(add5);
                pst.close();
                rs.close();
            }
            else {
                JOptionPane.showMessageDialog(null, "There is no data under that LinkNo!" );
            }
            
            con.commit();
        }
        catch(SQLException e) {
            JOptionPane.showMessageDialog(null, e);

        }
    }//GEN-LAST:event_SearchLinkButtonActionPerformed

    private void AddLinkButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddLinkButtonActionPerformed
        try{
            String sqladd = "INSERT into Link values(?,?,?,?,?)";
            PreparedStatement pst = con.prepareStatement(sqladd);
            pst.setInt(1, Integer.parseInt(LinkNoTextField.getText()));
            pst.setInt(2, Integer.parseInt(Person_IDTextField.getText()));
            pst.setInt(3, Integer.parseInt(TopicNoTextField.getText()));
            pst.setInt(4, Integer.parseInt((String) LevelOnTopicjSpinner.getValue()));
            pst.setInt(5, Integer.parseInt((String) PassionLeveljSpinner.getValue()));
            pst.execute();
            JOptionPane.showMessageDialog(null, "Added!");
            
            con.commit();
            
            //pst.close();
        }
        catch(HeadlessException | NumberFormatException | SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
        AddLinkButton.setEnabled(false);
        Link lk = new Link();
        lk.setVisible(true);
        dispose();
        //this.setVisible(false);
       
    }//GEN-LAST:event_AddLinkButtonActionPerformed

